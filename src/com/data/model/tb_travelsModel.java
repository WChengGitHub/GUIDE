package com.data.model;

public class tb_travelsModel {
	private String Tid;
	private String Text;
	private String Vid;
	private String Time;
	private String Sid;
	private String Publicty;
	public String getTid() {
		return Tid;
	}
	public void setTid(String tid) {
		Tid = tid;
	}
	public String getText() {
		return Text;
	}
	public void setText(String text) {
		Text = text;
	}
	public String getVid() {
		return Vid;
	}
	public void setVid(String vid) {
		Vid = vid;
	}
	public String getTime() {
		return Time;
	}
	public void setTime(String time) {
		Time = time;
	}
	public String getSid() {
		return Sid;
	}
	public void setSid(String sid) {
		Sid = sid;
	}
	public String getPublicty() {
		return Publicty;
	}
	public void setPublicty(String publicty) {
		Publicty = publicty;
	}
	
}
